/** Meta infinity mark (official gradients). */
export function MetaMark({ size = 48 }: { size?: number }) {
  const uid = useId();
  const gid = (i: number) => `${uid}-meta-${i}`;
  return (
    <svg width={size} height={size} viewBox="0 0 24 24" aria-hidden="true" style={{ flex: "none" }}>
      <defs>
        {META_GRADS.map((g, i) => (
          <linearGradient key={i} id={gid(i)} x1={g.x1} x2={g.x2} y1={g.y1} y2={g.y2}>
            {g.stops.map((s, j) => (
              <stop key={j} offset={s.offset} stopColor={s.color} />
            ))}
          </linearGradient>
        ))}
      </defs>
      {META_PATHS.map((p, i) => (
        <path key={i} d={p.d} fill={p.grad === null ? "#0082FB" : `url(#${gid(p.grad)})`} />
      ))}
    </svg>
  );
}

